<template>
  <div id="login">
    <div class="login-form">
      <a-form
        ref="formRef"
        :rules="formRules"
        :model="formState"
        :label-col="labelCol"
        :wrapper-col="wrapperCol"
      >
        <a-form-item>
          <img class="logo" src="@/assets/image/logo.png" />
          <div class="head">Medusa Admin</div>
          <div class="desc">打造通用基础建设，构建数字化技术中台</div>
        </a-form-item>
        <a-form-item ref="loginName" name="loginName">
          <a-input
            placeholder="输 入 账 户"
            v-model:value="formState.loginName"
          />
        </a-form-item>
        <a-form-item ref="password" name="password">
          <a-input
            placeholder="输 入 密 码"
            type="password"
            v-model:value="formState.password"
          />
        </a-form-item>
        <a-form-item>
          <a-checkbox :checked="true"> 记住我 </a-checkbox>
          <a class="forgot" href=""> 忘记密码 </a>
        </a-form-item>
        <a-form-item :wrapper-col="{ span: 24 }">
          <a-button :loading="load" type="primary" @clickr="onSubmit">
            登录
          </a-button>
        </a-form-item>
      </a-form>
    </div>
  </div>
</template>
<script>
import { reactive, ref, onMounted } from "vue";
import { useRouter } from "vue-router";
import { useStore } from "vuex";
// import { create } from "@/api/module/captcha";
import { notification } from "ant-design-vue";
export default {
  setup() {
    const router = useRouter();
    const store = useStore();
    const load = ref(false);

    const formRef = ref();

    // 登录参数
    const formState = reactive({
      loginName: "",
      password: ""
      // captchaKey: "key",
      // captchaCode: "code",
      // captchaImage: "image",
    });
    onMounted(() => {
      enterKeyup();
    });
    const formRules = {
      loginName: [{ required: true, message: "请输入账户", trigger: "blur" }],
      password: [{ required: true, message: "请输入密码", trigger: "blur" }]
    };

    // 验证码 初始化
    // const refreshCaptcha = async function () {
    //   const result = await create();
    //   formState.captchaKey = result.data.key;
    //   formState.captchaCode = result.data.code;
    //   formState.captchaImage = result.data.image;
    // };

    // refreshCaptcha();

    // 登录验证
    const onSubmit = () => {
      formRef.value
        .validate()
        .then(async () => {
          load.value = true;
          await store.dispatch("user/login", formState);
          load.value = false;
          notification["success"]({
            message: "登录成功",
            description: "欢迎."
          });
          await router.push("/");
        })
        .catch(error => {
          notification["error"]({
            message: "登录失败",
            description: error
          });
          load.value = false;
          // refreshCaptcha();
        });
    };
    const enterKey = event => {
      const code = event.keyCode
        ? event.keyCode
        : event.which
        ? event.which
        : event.charCode;
      if (code == 13) {
        onSubmit();
      }
    };
    const enterKeyupDestroyed = () => {
      document.removeEventListener("keyup", enterKey);
    };
    const enterKeyup = () => {
      document.addEventListener("keyup", enterKey);
    };
    return {
      labelCol: { span: 6 },
      wrapperCol: { span: 24 },
      // refreshCaptcha,
      formRules,
      formState,
      onSubmit,
      formRef,
      load,
      enterKey,
      enterKeyupDestroyed,
      enterKeyup
    };
  }
};
</script>

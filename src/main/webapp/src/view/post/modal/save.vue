<template>
  <a-modal
    :visible="visible"
    title="新增岗位"
    cancelText="取消"
    okText="提交"
    @ok="submit"
    @cancel="cancel"
  >
    <a-form
      ref="formRef"
      :model="formState"
      :rules="formRules"
      :label-col="labelCol"
      :wrapper-col="wrapperCol"
    >
      <a-form-item ref="postName" label="名称" name="postName">
        <a-input v-model:value="formState.postName" />
      </a-form-item>
      <a-form-item ref="postCode" label="标识" name="postCode">
        <a-input
          v-model:value="formState.postCode"
          placeholder="示例值(P-01)"
        />
      </a-form-item>
    </a-form>
  </a-modal>
</template>
<script>
import { message } from "ant-design-vue";
import { save } from "@/api/module/post";
import { defineComponent, reactive, ref, toRaw } from "vue";
export default defineComponent({
  props: {
    visible: {
      type: Boolean
    }
  },
  emit: ["close"],
  setup(props, context) {
    const formRef = ref();

    const formState = reactive({
      sort: 0,
      flag: false
    });

    const formRules = {
      postName: [
        { required: true, message: "请输入岗位名称", trigger: "blur" }
      ],
      postCode: [{ required: true, message: "请输入岗位编号", trigger: "blur" }]
    };

    const saveKey = "save";

    const submit = e => {
      message.loading({ content: "提交中...", key: saveKey });
      formRef.value
        .validate()
        .then(() => {
          save(toRaw(formState)).then(response => {
            if (response.success) {
              message
                .success({ content: "保存成功", key: saveKey, duration: 1 })
                .then(() => {
                  cancel(true);
                });
            } else {
              message
                .success({ content: "保存失败", key: saveKey, duration: 1 })
                .then(() => {
                  cancel();
                });
            }
          });
        })
        .catch(error => {
          console.log("error", error);
        });
    };

    const cancel = (isReload = false) => {
      formRef.value.resetFields();
      context.emit("close", isReload);
    };

    return {
      submit,
      cancel,
      formRef,
      formState,
      formRules,

      labelCol: { span: 6 },
      wrapperCol: { span: 18 }
    };
  }
});
</script>
